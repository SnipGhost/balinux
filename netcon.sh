 #!/bin/bash
#--------------------------------------------------------------------------------------------------
# Test filename var
#--------------------------------------------------------------------------------------------------
if [ -z $1 ]
then
	BASEDIR=`dirname $0`
	DIRPATH=`cd $BASEDIR; pwd`
    f=$DIRPATH/data/`date +%Y.%m.%d_%H:%M:%S_netcon`
else
    f="$1"
fi
#--------------------------------------------------------------------------------------------------
printf "<table border=\"1\">\n" > $f
ss | grep -v -E '(u_str)' | sed -e 's/Local /Local_/g' | sed 's/Peer /Peer_/g' | awk              \
'{ split($0, k);                                                                                  \
   print "<tr>";                                                                                  \
   { for (i = 1; i <= 6; i++) print "<td>"k[i]"</td>"; }                                          \
   print "</tr>"; }' >> $f
printf "\n</table>\n" >> $f
