#!/bin/bash
#------------------------------------------------------------------------------
# Test timeout time
#------------------------------------------------------------------------------
if [ -z $1 ]
then
    t="20"
else
    t="$1"
fi
#------------------------------------------------------------------------------
# Test packs count
#------------------------------------------------------------------------------
if [ -z $2 ]
then
    p="500"
else
    p="$2"
fi
#------------------------------------------------------------------------------
# Test filename var
#------------------------------------------------------------------------------
if [ -z $3 ]
then
    BASEDIR=`dirname $0`
    DIRPATH=`cd $BASEDIR; pwd`
    f=$DIRPATH/data/`date +%Y.%m.%d_%H:%M:%S_toptlk`
else
    f="$3"
fi
#------------------------------------------------------------------------------
# Print top talkers
#------------------------------------------------------------------------------
printf "<table border=\"1\">\n" > $f
sudo timeout $t tcpdump -tnn -c $p | awk -F "." \
'{print $1"."$2"."$3"."$4}' | sort | uniq -c | sort -nr | awk '$1 > 1' | awk -F " " \
'{ print "<tr><td>"NR"</td><td>"$1"</td><td>"$2 $3"</td></tr>" }' >> $f
printf "\n</table>\n" >> $f
#------------------------------------------------------------------------------